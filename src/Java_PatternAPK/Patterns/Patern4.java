package Java_PatternAPK.Patterns;

/**
 AAAAA
 BBBBB
 CCCCC
 DDDDD
 EEEEE
 */
public class Patern4 {
    public static void main(String[] args) {
        char x;
        for (x='A';x<='E';x++){
            for (int z = 0;z<5;z++){
                System.out.print(x);
            }
            System.out.println();
        }
    }
}

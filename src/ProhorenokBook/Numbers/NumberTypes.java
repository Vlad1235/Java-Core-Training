package ProhorenokBook.Numbers;

public class NumberTypes {
    public static void main(String[] args) {
        int x, y, z, l;
        x = 119; // десятиричный
        y = 0167; // восмеричный
        z = 0x77; // шастнадцатиричный
        l = 0B01110111; // двоичный
        System.out.println(x + " " + y + " " + z + " " + l); //119 119 119 119
    }
}

package ProhorenokBook.String_about;

/**
 * Заменяет все символы строки строчными буквами или прописными если метод toUpperCase()
 */
public class ToLowerCase {
    public static void main(String[] args) {
        System.out.println("СТРОКА".toLowerCase()); // строка
        System.out.println("строка".toUpperCase()); //СТРОКА
    }
}
